    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        for(int r=0;r<size;r++)
        {
            for(int c=0;c<7;c++)
            {
                String value = table1.getModel().getValueAt(r, c).toString();
                if(!value.equals(tab[r][c]))
                {
                    tab[r][c] = value;
                    
                    try
                    {
                        Class.forName("com.mysql.jdbc.Driver");

                        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quizza","root","");

                        st = con.createStatement();
                        String query = "UPDATE `"+subject+"`.`"+testname+"` SET `Q_No`='"+table1.getModel().getValueAt(r, 0).toString()+"',"+
                                "`Question`='"+table1.getModel().getValueAt(r, 1).toString()+"',"+
                                "`option1`='"+table1.getModel().getValueAt(r, 2).toString()+"',"+
                                "`option2`='"+table1.getModel().getValueAt(r, 3).toString()+"',"+
                                "`option3`='"+table1.getModel().getValueAt(r, 4).toString()+"',"+
                                "`option4`='"+table1.getModel().getValueAt(r, 5).toString()+"',"+
                                "`correct`='"+table1.getModel().getValueAt(r, 6).toString()+"'"+" WHERE `Q_No`='"+table1.getModel().getValueAt(r, 0).toString()+"'";
                        
                        st.executeUpdate(query);
                        con.close();
                        if(online)
                            new ViewPreviousTest(username,subject,true).setVisible(true);
                        else
                            new ViewPreviousTest(username,subject,false).setVisible(true);
                        this.setVisible(false);
                    }
                    catch (ClassNotFoundException e)
                    {
                        System.out.println(e);
                    }
                    catch(SQLException e)
                    {
                        System.out.println(e);
                        JFrame f = new JFrame();
                        JOptionPane.showMessageDialog(f, "Could Not Update");
                    }
                }
            }
        }
    }//GEN-LAST:event_UpdateActionPerformed

