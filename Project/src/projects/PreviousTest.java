    public ArrayList<User> test()
    {
        ArrayList<User> test = new ArrayList<>();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            //System.out.println("Driver Loaded");

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quizza","root","");
            //System.out.println("Connected");

            st = con.createStatement();
            String query = "SELECT * FROM `"+subject+"`.`"+testname+"`";
            rs = st.executeQuery(query);
            User user;
            while(rs.next())
            {
                user = new User(rs.getInt("Q_No"),rs.getString("Question"),rs.getString("option1"),rs.getString("option2"),rs.getString("option3"),rs.getString("option4"),rs.getString("correct"));
                test.add(user);
            }
            con.close();
        }
        catch (ClassNotFoundException e)
        {
            System.out.println(e);
        }
        catch(SQLException e)
        {
            System.out.println(e);
        }
        return test;
    }
    
    public void show_test()
    {
        //table1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        ArrayList<User> list = test();
        size = list.size();
        tab = new String[size][7];
        DefaultTableModel model = (DefaultTableModel)table1.getModel();
        Object[] row = new Object[7];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getQuestionNo();
            row[1]=list.get(i).getQuestion();
            row[2]=list.get(i).getOption1();
            row[3]=list.get(i).getOption2();
            row[4]=list.get(i).getOption3();
            row[5]=list.get(i).getOption4();
            row[6]=list.get(i).getCorrect();
            model.addRow(row);
        }
        for(int r=0;r<size;r++)
        {
            for(int c=0;c<7;c++)
            {
                tab[r][c] = table1.getModel().getValueAt(r, c).toString();
            }
        }
    }

