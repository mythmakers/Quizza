    private void editusernameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editusernameKeyReleased
        char ch = evt.getKeyChar();
        if(Character.isWhitespace(ch) || Character.isUpperCase(ch) || (!Character.isAlphabetic(ch) && !(ch==java.awt.event.KeyEvent.VK_BACK_SPACE) && !(ch==java.awt.event.KeyEvent.VK_DELETE) && ch!='.' && ch!='_'))
        {
            getToolkit().beep();
            evt.consume();
        }
        if(!editusername.getText().equals(username) && !editusername.getText().trim().equals(""))
        {
            Ueditbtn.setEnabled(true);
        }
        else
        {
            Ueditbtn.setEnabled(false);
        }
        if(ch==KeyEvent.VK_ENTER)
        {
            Ueditbtn.doClick();
        }
    }//GEN-LAST:event_editusernameKeyReleased

