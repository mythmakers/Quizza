    private void editcontactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editcontactKeyTyped
        char ch = evt.getKeyChar();
        if(ch==KeyEvent.VK_BACKSPACE || ch==KeyEvent.VK_DELETE || editcontact.getText().length()==10)
        {
            if(!editcontact.getText().equals(contact) && (editcontact.getText()).length()==10)
            {
                Ceditbtn.setEnabled(true);
            }
            else
                Ceditbtn.setEnabled(false);
        }
        else
        {
            if(!(editcontact.getText()+ch).equals(contact) && (editcontact.getText()+ch).length()==10)
            {
                Ceditbtn.setEnabled(true);
            }
            else
                Ceditbtn.setEnabled(false);
        }
        if(!(Character.isDigit(ch) || (ch==KeyEvent.VK_BACKSPACE) || ch==KeyEvent.VK_DELETE))
        {
            getToolkit().beep();
            evt.consume();
        }
        if(editcontact.getText().length()==10)
        {
            if(ch!=KeyEvent.VK_BACKSPACE || ch!=KeyEvent.VK_DELETE)
            {   
                getToolkit().beep();
                evt.consume();
            }
        }
        if(ch==KeyEvent.VK_ENTER)
        {
            Ceditbtn.doClick();
        }
    }//GEN-LAST:event_editcontactKeyTyped

