    private void UeditbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UeditbtnActionPerformed
        if(Ueditbtn.getText().equals("Update"))
        {
            if(Character.isAlphabetic(editusername.getText().charAt(0)))
            {
                try{
                    String query = "UPDATE `quizza`.`createtable` SET `username`='"+editusername.getText()+"',"+
                                        "`name`='"+name+"',"+
                                        "`password`='"+password+"',"+
                                        "`email`='"+email+"',"+
                                        "`subject`='"+subject+"',"+
                                        "`contact`='"+contact+"',"+
                                        " `gender`='"+gender+"' WHERE `username`='"+username+"'";
                    st.executeUpdate(query);
                    username = editusername.getText();
                    new Profile(username,subject).setVisible(true);
                    this.setVisible(false);
                }
                catch(MySQLIntegrityConstraintViolationException e)
                {
                    JFrame f = new JFrame();
                    JOptionPane.showMessageDialog(f, "username already exists", "Error", JOptionPane.WARNING_MESSAGE);
                }
                catch(SQLException e)
                {
                    System.out.println(e);
                }
            }
            else
            {
                validusernameError.setVisible(true);
            }
        }
        if(Ueditbtn.getText().equals("Edit"))
        {
            editusername.setEnabled(true);
            Ueditbtn.setText("Update");
            Ueditbtn.setEnabled(false);
        }
    }//GEN-LAST:event_UeditbtnActionPerformed

