    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        String username=uname.getText();
        char pass[]=upass.getPassword();
        String password=new String(pass);
        String tablePassword = "";
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            //System.out.println("Driver Loaded");

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quizza","root","");
            //System.out.println("Connected");

            st = con.createStatement();
            String query="SELECT password from adminlogin where username='"+username+"'";
            rs=st.executeQuery(query);

            if(rs.next())
            {
                tablePassword=rs.getString(1);
            }
            else
            {
                message.setVisible(true);

            }
            con.close();
            if(username!=null && password!=null && !username.trim().equals("") && password.equals(tablePassword))
            {
                new AdminHome(username).setVisible(true);
                this.setVisible(false);
                //System.out.println("Login Successful");
            }
            else
            {
                message.setVisible(true);
                upass.setText("");
                //System.out.println("Login Failed...");
            }
        }
        catch(CommunicationsException e)
        {
            upass.setText("");
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Not Connected to the Server!\nPlease Connect to server to Login.", "Connection", JOptionPane.ERROR_MESSAGE);
        }
        catch (ClassNotFoundException e)
        {
            System.out.println(e);
        }
        catch(SQLException e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_LoginActionPerformed

