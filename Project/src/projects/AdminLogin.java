    private void unameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_unameKeyTyped
        int ch = evt.getKeyChar();
        if (Character.isWhitespace(ch) || Character.isUpperCase(ch) || (!Character.isAlphabetic(ch) && !(ch == KeyEvent.VK_BACK_SPACE) && !(ch == KeyEvent.VK_DELETE) && ch != '.' && ch != '_')) {
            getToolkit().beep();
            evt.consume();
        }
        if(ch==KeyEvent.VK_ENTER)
        {
            Login.doClick();
        }
    }//GEN-LAST:event_unameKeyTyped

